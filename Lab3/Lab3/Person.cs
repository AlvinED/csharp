using System;

namespace Lab3Hard_Solution
{
    public class Person
    {
        ///////////////////////////////////////////////////////////////////////
        ////////////////////////// Member Variables ///////////////////////////
        ///////////////////////////////////////////////////////////////////////

        public string FirstName { get; set; }
        public string LastName { get; set; }
        public int Age { get; set; }
        public Person Spouse { get; set; }

        ///////////////////////////////////////////////////////////////////////
        ////////////////////////// Static Variables ///////////////////////////
        ///////////////////////////////////////////////////////////////////////

        public static double AverageAge;
        public static Person Youngest;
        public static Person Oldest;

        ///////////////////////////////////////////////////////////////////////
        /////////////////////////// Member Methods ////////////////////////////
        ///////////////////////////////////////////////////////////////////////

        public bool IsMarried()
        {
            return Spouse != null;
        }

        public string GetNameAndAge()
        {
            return FirstName + " " + LastName + " (" + Age + ")";
        }

        ///////////////////////////////////////////////////////////////////////
        /////////////////////////// Static Methods ////////////////////////////
        ///////////////////////////////////////////////////////////////////////

        public static void PrintStaticValues()
        {
            Console.WriteLine("Average Age : " + AverageAge);
            Console.WriteLine("Youngest    : " + Youngest.GetNameAndAge());
            Console.WriteLine("Oldest      : " + Oldest.GetNameAndAge());
            Console.ReadLine();
        }

        ///////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////
    }
}